CREATE TABLE IF NOT EXISTS public.clientes (
  id          uuid        NOT NULL,
  nome        text        NOT NULL,
  email       text        NOT NULL,
  cnpj        text        NOT NULL,
  role        text        NOT NULL DEFAULT 'cliente_ingapan',
  created_at  timestamptz DEFAULT now(),
  updated_at  timestamptz DEFAULT now(),
  CONSTRAINT clientes_pkey       PRIMARY KEY (id),
  CONSTRAINT clientes_email_key  UNIQUE (email),
  CONSTRAINT clientes_cnpj_key   UNIQUE (cnpj),
  CONSTRAINT clientes_role_check CHECK (role IN ('cliente_ingapan', 'admin_ingapan')),
  CONSTRAINT clientes_id_fkey    FOREIGN KEY (id)
    REFERENCES auth.users (id) ON DELETE CASCADE
);

CREATE TABLE IF NOT EXISTS public.product_categoria (
  id        integer GENERATED BY DEFAULT AS IDENTITY,
  category  text    NOT NULL,
  CONSTRAINT product_categoria_pkey          PRIMARY KEY (id),
  CONSTRAINT product_categoria_category_key  UNIQUE (category)
);

CREATE TABLE IF NOT EXISTS public.products (
  id            bigint  GENERATED BY DEFAULT AS IDENTITY,
  nome          varchar,
  categoria     varchar,
  descricao     varchar,
  image_url     text,
  id_categoria  integer,
  CONSTRAINT products_pkey               PRIMARY KEY (id),
  CONSTRAINT products_id_categoria_fkey  FOREIGN KEY (id_categoria)
    REFERENCES public.product_categoria (id)
    ON UPDATE CASCADE ON DELETE RESTRICT
);

CREATE INDEX IF NOT EXISTS products_categoria_idx     ON public.products (categoria);
CREATE INDEX IF NOT EXISTS products_id_categoria_idx  ON public.products (id_categoria);
CREATE INDEX IF NOT EXISTS products_nome_idx          ON public.products (nome);

CREATE TABLE IF NOT EXISTS public.products_featured (
  id            bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  product_id    bigint NOT NULL UNIQUE,
  display_order integer NOT NULL DEFAULT 1 CHECK (display_order > 0),
  created_at    timestamptz NOT NULL DEFAULT now(),
  CONSTRAINT products_featured_product_id_fkey
    FOREIGN KEY (product_id)
    REFERENCES public.products (id)
    ON UPDATE CASCADE
    ON DELETE CASCADE
);

CREATE INDEX IF NOT EXISTS products_featured_display_order_idx
  ON public.products_featured (display_order, id);

CREATE TABLE IF NOT EXISTS public.product_stats (
  product_id  bigint PRIMARY KEY,
  views_count bigint NOT NULL DEFAULT 0,
  updated_at  timestamptz NOT NULL DEFAULT now(),
  CONSTRAINT product_stats_product_id_fkey
    FOREIGN KEY (product_id)
    REFERENCES public.products (id)
    ON UPDATE CASCADE
    ON DELETE CASCADE
);

CREATE TABLE IF NOT EXISTS public.product_sales_stats (
  product_id    bigint PRIMARY KEY
    REFERENCES public.products (id)
    ON UPDATE CASCADE
    ON DELETE CASCADE,
  orders_count  bigint NOT NULL DEFAULT 0 CHECK (orders_count >= 0),
  units_sold    bigint NOT NULL DEFAULT 0 CHECK (units_sold >= 0),
  updated_at    timestamptz NOT NULL DEFAULT now()
);

CREATE INDEX IF NOT EXISTS product_sales_stats_units_sold_desc_idx
  ON public.product_sales_stats (units_sold DESC);
CREATE INDEX IF NOT EXISTS product_sales_stats_orders_count_desc_idx
  ON public.product_sales_stats (orders_count DESC);

CREATE TABLE IF NOT EXISTS public.client_orders (
  id             bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  session_id     text NOT NULL,
  user_id        uuid NULL REFERENCES auth.users (id) ON DELETE SET NULL,
  customer_name  text NULL,
  customer_email text NULL,
  created_at     timestamptz NOT NULL DEFAULT now(),
  CONSTRAINT client_orders_session_id_not_empty CHECK (length(btrim(session_id)) > 0)
);

CREATE INDEX IF NOT EXISTS client_orders_created_at_idx
  ON public.client_orders (created_at DESC);
CREATE INDEX IF NOT EXISTS client_orders_user_created_at_idx
  ON public.client_orders (user_id, created_at DESC);

CREATE TABLE IF NOT EXISTS public.client_order_items (
  id                    bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  order_id              bigint NOT NULL REFERENCES public.client_orders (id) ON DELETE CASCADE,
  product_id            bigint NULL REFERENCES public.products (id) ON DELETE SET NULL,
  product_name_snapshot text NOT NULL,
  quantity              integer NOT NULL CHECK (quantity > 0),
  created_at            timestamptz NOT NULL DEFAULT now(),
  CONSTRAINT client_order_items_unique_order_product UNIQUE (order_id, product_id)
);

CREATE INDEX IF NOT EXISTS client_order_items_order_id_idx
  ON public.client_order_items (order_id);


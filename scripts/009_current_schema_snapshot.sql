CREATE OR REPLACE FUNCTION public.handle_new_cliente()
RETURNS trigger
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path TO 'public'
AS $function$
BEGIN
  INSERT INTO public.clientes (id, nome, email, cnpj, role)
  VALUES (
    NEW.id,
    COALESCE(NEW.raw_user_meta_data ->> 'nome', ''),
    NEW.email,
    COALESCE(NEW.raw_user_meta_data ->> 'cnpj', ''),
    'cliente_ingapan'
  )
  ON CONFLICT (id) DO NOTHING;

  RETURN NEW;
END;
$function$;

CREATE OR REPLACE FUNCTION public.is_admin_ingapan(target_user_id uuid DEFAULT auth.uid())
RETURNS boolean
LANGUAGE sql
STABLE
SECURITY DEFINER
SET search_path TO 'public'
AS $function$
  SELECT EXISTS (
    SELECT 1
    FROM public.clientes c
    WHERE c.id = target_user_id
      AND lower(trim(c.role)) = 'admin_ingapan'
  );
$function$;

CREATE OR REPLACE FUNCTION public.increment_product_view(target_product_id bigint)
RETURNS void
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path TO 'public'
AS $function$
BEGIN
  IF target_product_id IS NULL OR target_product_id <= 0 THEN
    RAISE EXCEPTION 'target_product_id invalido';
  END IF;

  INSERT INTO public.product_stats (product_id, views_count, updated_at)
  VALUES (target_product_id, 1, now())
  ON CONFLICT (product_id)
  DO UPDATE
  SET
    views_count = public.product_stats.views_count + 1,
    updated_at = now();
END;
$function$;

CREATE OR REPLACE FUNCTION public.enforce_products_featured_limit()
RETURNS trigger
LANGUAGE plpgsql
AS $$
DECLARE
  featured_count integer;
BEGIN
  SELECT COUNT(*) INTO featured_count FROM public.products_featured;
  IF featured_count >= 10 THEN
    RAISE EXCEPTION 'Limite de 10 produtos em destaque atingido.'
      USING ERRCODE = '23514';
  END IF;
  RETURN NEW;
END;
$$;

CREATE OR REPLACE FUNCTION public.create_client_order(p_session_id text, p_items jsonb)
RETURNS bigint
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path TO 'public'
AS $function$
DECLARE
  v_user_id uuid := auth.uid();
  v_order_id bigint;
  v_session_id text := btrim(COALESCE(p_session_id, ''));
  v_customer_name text;
  v_customer_email text;
  v_item_count integer;
  v_inserted_item_count integer;
BEGIN
  IF v_session_id = '' OR length(v_session_id) > 128 THEN
    RAISE EXCEPTION 'session_id invalido';
  END IF;

  IF jsonb_typeof(p_items) IS DISTINCT FROM 'array' THEN
    RAISE EXCEPTION 'items invalidos';
  END IF;

  WITH normalized_items AS (
    SELECT
      (item ->> 'product_id')::bigint AS product_id,
      (item ->> 'quantity')::integer AS quantity
    FROM jsonb_array_elements(p_items) AS item
  ),
  sanitized_items AS (
    SELECT
      ni.product_id,
      SUM(ni.quantity)::integer AS quantity
    FROM normalized_items ni
    WHERE ni.product_id IS NOT NULL
      AND ni.product_id > 0
      AND ni.quantity IS NOT NULL
      AND ni.quantity > 0
      AND ni.quantity <= 999
    GROUP BY ni.product_id
  )
  SELECT COUNT(*)
  INTO v_item_count
  FROM sanitized_items;

  IF v_item_count = 0 THEN
    RAISE EXCEPTION 'nenhum item valido';
  END IF;

  IF v_item_count > 100 THEN
    RAISE EXCEPTION 'limite de 100 itens distintos por pedido';
  END IF;

  IF v_user_id IS NOT NULL THEN
    SELECT c.nome, c.email
    INTO v_customer_name, v_customer_email
    FROM public.clientes c
    WHERE c.id = v_user_id;
  END IF;

  INSERT INTO public.client_orders (
    session_id,
    user_id,
    customer_name,
    customer_email
  )
  VALUES (
    v_session_id,
    v_user_id,
    v_customer_name,
    v_customer_email
  )
  RETURNING id INTO v_order_id;

  WITH normalized_items AS (
    SELECT
      (item ->> 'product_id')::bigint AS product_id,
      (item ->> 'quantity')::integer AS quantity
    FROM jsonb_array_elements(p_items) AS item
  ),
  sanitized_items AS (
    SELECT
      ni.product_id,
      SUM(ni.quantity)::integer AS quantity
    FROM normalized_items ni
    WHERE ni.product_id IS NOT NULL
      AND ni.product_id > 0
      AND ni.quantity IS NOT NULL
      AND ni.quantity > 0
      AND ni.quantity <= 999
    GROUP BY ni.product_id
  ),
  validated_products AS (
    SELECT
      si.product_id,
      p.nome AS product_name_snapshot,
      si.quantity
    FROM sanitized_items si
    INNER JOIN public.products p
      ON p.id = si.product_id
  )
  INSERT INTO public.client_order_items (
    order_id,
    product_id,
    product_name_snapshot,
    quantity
  )
  SELECT
    v_order_id,
    vp.product_id,
    vp.product_name_snapshot,
    vp.quantity
  FROM validated_products vp;

  GET DIAGNOSTICS v_inserted_item_count = ROW_COUNT;

  IF v_inserted_item_count <> v_item_count THEN
    RAISE EXCEPTION 'um ou mais produtos informados sao invalidos';
  END IF;

  RETURN v_order_id;
END;
$function$;

CREATE OR REPLACE FUNCTION public.apply_product_sales_stats_delta(
  p_product_id bigint,
  p_orders_delta integer,
  p_units_delta integer
)
RETURNS void
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path TO 'public'
AS $function$
BEGIN
  IF p_product_id IS NULL OR p_product_id <= 0 THEN
    RETURN;
  END IF;

  IF p_orders_delta = 0 AND p_units_delta = 0 THEN
    RETURN;
  END IF;

  INSERT INTO public.product_sales_stats (product_id, orders_count, units_sold, updated_at)
  VALUES (
    p_product_id,
    GREATEST(p_orders_delta, 0)::bigint,
    GREATEST(p_units_delta, 0)::bigint,
    now()
  )
  ON CONFLICT (product_id)
  DO UPDATE
  SET
    orders_count = GREATEST(0, public.product_sales_stats.orders_count + p_orders_delta),
    units_sold = GREATEST(0, public.product_sales_stats.units_sold + p_units_delta),
    updated_at = now();

  DELETE FROM public.product_sales_stats
  WHERE product_id = p_product_id
    AND orders_count = 0
    AND units_sold = 0;
END;
$function$;

CREATE OR REPLACE FUNCTION public.sync_product_sales_stats_from_order_items()
RETURNS trigger
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path TO 'public'
AS $function$
BEGIN
  IF TG_OP = 'INSERT' THEN
    PERFORM public.apply_product_sales_stats_delta(NEW.product_id, 1, NEW.quantity);
    RETURN NEW;
  END IF;

  IF TG_OP = 'UPDATE' THEN
    IF OLD.product_id IS NOT NULL AND NEW.product_id IS NOT NULL AND OLD.product_id = NEW.product_id THEN
      PERFORM public.apply_product_sales_stats_delta(NEW.product_id, 0, NEW.quantity - OLD.quantity);
      RETURN NEW;
    END IF;

    PERFORM public.apply_product_sales_stats_delta(OLD.product_id, -1, -OLD.quantity);
    PERFORM public.apply_product_sales_stats_delta(NEW.product_id, 1, NEW.quantity);
    RETURN NEW;
  END IF;

  IF TG_OP = 'DELETE' THEN
    PERFORM public.apply_product_sales_stats_delta(OLD.product_id, -1, -OLD.quantity);
    RETURN OLD;
  END IF;

  RETURN NULL;
END;
$function$;


-- ----------------------------------------------------------------------------
-- POLICIES: public.clientes
-- ----------------------------------------------------------------------------

-- Admins podem ler todos os clientes
DROP POLICY IF EXISTS admins_select_all ON public.clientes;
CREATE POLICY admins_select_all
  ON public.clientes
  FOR SELECT
  TO public
  USING (public.is_admin_ingapan(auth.uid()));

-- Admins podem atualizar qualquer cliente
DROP POLICY IF EXISTS admins_update_all ON public.clientes;
CREATE POLICY admins_update_all
  ON public.clientes
  FOR UPDATE
  TO public
  USING (public.is_admin_ingapan(auth.uid()));

-- Cliente pode inserir apenas seu proprio registro (id = auth.uid())
DROP POLICY IF EXISTS clientes_insert_own ON public.clientes;
CREATE POLICY clientes_insert_own
  ON public.clientes
  FOR INSERT
  TO public
  WITH CHECK (auth.uid() = id);

-- Cliente pode ler o proprio registro
DROP POLICY IF EXISTS clientes_select_own ON public.clientes;
CREATE POLICY clientes_select_own
  ON public.clientes
  FOR SELECT
  TO public
  USING (auth.uid() = id);

-- Cliente pode atualizar o proprio registro
DROP POLICY IF EXISTS clientes_update_own ON public.clientes;
CREATE POLICY clientes_update_own
  ON public.clientes
  FOR UPDATE
  TO public
  USING (auth.uid() = id);

-- ----------------------------------------------------------------------------
-- POLICIES: public.products
-- ----------------------------------------------------------------------------

-- Leitura publica do catalogo (qualquer visitante)
DROP POLICY IF EXISTS products_public_select ON public.products;
CREATE POLICY products_public_select
  ON public.products
  FOR SELECT
  TO public
  USING (true);

-- Admin pode ler (redundante com a public_select, mas mantida conforme banco)
DROP POLICY IF EXISTS products_admin_select ON public.products;
CREATE POLICY products_admin_select
  ON public.products
  FOR SELECT
  TO public
  USING (public.is_admin_ingapan(auth.uid()));

-- Apenas admin pode inserir
DROP POLICY IF EXISTS products_admin_insert ON public.products;
CREATE POLICY products_admin_insert
  ON public.products
  FOR INSERT
  TO public
  WITH CHECK (public.is_admin_ingapan(auth.uid()));

-- Apenas admin pode atualizar
DROP POLICY IF EXISTS products_admin_update ON public.products;
CREATE POLICY products_admin_update
  ON public.products
  FOR UPDATE
  TO public
  USING (public.is_admin_ingapan(auth.uid()))
  WITH CHECK (public.is_admin_ingapan(auth.uid()));

-- Apenas admin pode deletar
DROP POLICY IF EXISTS products_admin_delete ON public.products;
CREATE POLICY products_admin_delete
  ON public.products
  FOR DELETE
  TO public
  USING (public.is_admin_ingapan(auth.uid()));


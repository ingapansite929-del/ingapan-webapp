-- Cria automaticamente um registro em public.clientes quando um novo usuario
-- e inserido em auth.users (executada via trigger on_auth_user_created).
CREATE OR REPLACE FUNCTION public.handle_new_cliente()
RETURNS trigger
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path TO 'public'
AS $function$
BEGIN
  INSERT INTO public.clientes (id, nome, email, cnpj, role)
  VALUES (
    NEW.id,
    COALESCE(NEW.raw_user_meta_data ->> 'nome', ''),
    NEW.email,
    COALESCE(NEW.raw_user_meta_data ->> 'cnpj', ''),
    'cliente_ingapan'
  )
  ON CONFLICT (id) DO NOTHING;

  RETURN NEW;
END;
$function$;

-- Retorna TRUE se o usuario informado (default auth.uid()) for admin_ingapan.
-- Usada pelas policies RLS para permitir acesso administrativo.
CREATE OR REPLACE FUNCTION public.is_admin_ingapan(target_user_id uuid DEFAULT auth.uid())
RETURNS boolean
LANGUAGE sql
STABLE
SECURITY DEFINER
SET search_path TO 'public'
AS $function$
  SELECT EXISTS (
    SELECT 1
    FROM public.clientes c
    WHERE c.id = target_user_id
      AND lower(trim(c.role)) = 'admin_ingapan'
  );
$function$;

-- ============================================================================
-- TRIGGERS
-- ============================================================================

-- Trigger em auth.users para popular public.clientes no signup.
DROP TRIGGER IF EXISTS on_auth_user_created ON auth.users;
CREATE TRIGGER on_auth_user_created
AFTER INSERT ON auth.users
FOR EACH ROW
EXECUTE FUNCTION public.handle_new_cliente();


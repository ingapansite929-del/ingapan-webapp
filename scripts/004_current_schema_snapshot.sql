CREATE EXTENSION IF NOT EXISTS "uuid-ossp";
CREATE EXTENSION IF NOT EXISTS "pgcrypto";

-- ============================================================================
-- TABELA: public.clientes
-- Armazena perfis dos clientes / admins vinculados a auth.users.
-- ============================================================================
CREATE TABLE IF NOT EXISTS public.clientes (
  id          uuid        NOT NULL,
  nome        text        NOT NULL,
  email       text        NOT NULL,
  cnpj        text        NOT NULL,
  role        text        NOT NULL DEFAULT 'cliente_ingapan',
  created_at  timestamptz DEFAULT now(),
  updated_at  timestamptz DEFAULT now(),
  CONSTRAINT clientes_pkey       PRIMARY KEY (id),
  CONSTRAINT clientes_email_key  UNIQUE (email),
  CONSTRAINT clientes_cnpj_key   UNIQUE (cnpj),
  CONSTRAINT clientes_role_check CHECK (role IN ('cliente_ingapan', 'admin_ingapan')),
  CONSTRAINT clientes_id_fkey    FOREIGN KEY (id)
    REFERENCES auth.users (id) ON DELETE CASCADE
);

-- ============================================================================
-- TABELA: public.product_categoria
-- Categorias de produtos (chave estrangeira usada por public.products).
-- ============================================================================
CREATE TABLE IF NOT EXISTS public.product_categoria (
  id        integer GENERATED BY DEFAULT AS IDENTITY,
  category  text    NOT NULL,
  CONSTRAINT product_categoria_pkey          PRIMARY KEY (id),
  CONSTRAINT product_categoria_category_key  UNIQUE (category)
);

-- ============================================================================
-- TABELA: public.products
-- Produtos do catalogo.
-- Observacao: a coluna "categoria" (varchar) permanece por compatibilidade,
-- mas o vinculo correto com a categoria e via "id_categoria" (FK).
-- ============================================================================
CREATE TABLE IF NOT EXISTS public.products (
  id            bigint  GENERATED BY DEFAULT AS IDENTITY,
  nome          varchar,
  categoria     varchar,
  descricao     varchar,
  image_url     text,
  id_categoria  integer,
  CONSTRAINT products_pkey               PRIMARY KEY (id),
  CONSTRAINT products_id_categoria_fkey  FOREIGN KEY (id_categoria)
    REFERENCES public.product_categoria (id)
    ON UPDATE CASCADE ON DELETE RESTRICT
);

-- Indices auxiliares
CREATE INDEX IF NOT EXISTS products_categoria_idx     ON public.products (categoria);
CREATE INDEX IF NOT EXISTS products_id_categoria_idx  ON public.products (id_categoria);
CREATE INDEX IF NOT EXISTS products_nome_idx          ON public.products (nome);

